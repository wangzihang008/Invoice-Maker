    private boolean isClientExisted(String name){
        boolean exist = false;
        connection c = new connection();
        try {
            ResultSet r = c.getResult("select * from school where school_name = '" + name +  "'");
            if(r.next()){
                exist = true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(ClientCreatePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        return exist;
    }

