    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        // TODO add your handling code here:
        String name = schoolNameText.getText();
        String shortName = schoolShortName.getText();
        String address = schoolAddress.getText();
        String phone = schoolPhone.getText();
        if(name.equals("")){
            JOptionPane.showMessageDialog(this, "School Name Cannot Be Empty!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(shortName.equals("")){
            JOptionPane.showMessageDialog(this, "School Short Name Cannot Be Empty!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(address.equals("")){
            JOptionPane.showMessageDialog(this, "School Address Cannot Be Empty!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(phone.equals("")){
            JOptionPane.showMessageDialog(this, "School Phone Number Cannot Be Empty!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(isClientExisted(name)){
            JOptionPane.showMessageDialog(this, "School Client Has Already Existed!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if(!name.equals("") && !shortName.equals("") && !address.equals("") && !phone.equals("")){
            school s = new school();
            s.setName(name);
            s.setShortName(shortName);
            s.setAddress(address);
            s.setPhone(phone);
            String sql = "insert into school (school_name, shcool_short_name, address, phone) values ('" + name + "','" + shortName + "','" + address + "','" + phone +"')";
            connection conn = new connection();
            conn.excute(sql);
            schoolNameText.setText("");
            schoolShortName.setText("");
            schoolAddress.setText("");
            schoolPhone.setText("");
            JOptionPane.showMessageDialog(this, "New School Client " + name + " Has Been Added.", "Sucessful", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_createButtonActionPerformed


